# Пять часто применяемых методов списка (list):
# append(element) - Добавление элемента в конец, где element (обязательный) - элемент, который нужно добавить в конец списка
# count(element) - Подсчет количества элементов, где element (обязательный) - элемент, количество вхождений которого нужно подсчитать в списке
# pop([index]) - Удаление и возврат элемента по индексу, где index (необязательный) - индекс элемента, который нужно удалить и вернуть (по умолчанию: -1 (последний элемент), должен быть целым числом в диапазоне -len(list) <= index < len(list))
# sort(key=None, reverse=False) - Сортировка списка, где key (необязательный) - функция для определения порядка сортировки, reverse (необязательный) - направление сортировки
# copy() - Создание копии списка, нет параметров - метод вызывается без аргументов


# Пять часто применяемых методов словаря (dict):
# keys() - Получение всех ключей, нет параметров - метод вызывается без аргументов
# values() - Получение всех значений, нет параметров - метод вызывается без аргументов
# items() - Получение пар ключ-значение, нет параметров - метод вызывается без аргументов
# update([other]) - Обновление словаря, где other (обязательный) - может быть: другим словарем, итерируемым объектом с парами ключ-значение, аргументами с ключевыми словами
# get(key[, default]) - Безопасное получение значения, где key (обязательный) - ключ для поиска в словаре, default (необязательный) - значение по умолчанию


# Пять часто применяемых методов множества (set):
# add(element) - Добавление элемента, где element (обязательный) - элемент для добавления в множество
# remove(element) - Удаление элемента, где element (обязательный) - элемент, который нужно удалить из множества
# discard(element) - Безопасное удаление элемента, где element (обязательный) - элемент, который нужно удалить из множества
# union(*others) - Объединение множеств, где *others (необязательные) - один или более итерируемых объектов для объединения
# intersection(*others) - Пересечение множеств, где *others (необязательные) - один или более итерируемых объектов для пересечения


# Пять часто применяемых методов строк (str):
# split(sep=None, maxsplit=-1) - Разделение строки на список, где sep (необязательный) - разделитель, maxsplit (необязательный) - максимальное количество разбиений
# upper() / lower() - Изменение регистра, нет параметров - методы вызываются без аргументов
# replace(old, new[, count]) - Замена подстрок, где old (обязательный) - подстрока, которую нужно найти и заменить; new (обязательный) - подстрока, на которую будет заменена подстрока old, count (необязательный) - количество замен
# join(iterable) - Объединение строк, где iterable (обязательный) - итерируемый объект, элементы которого нужно объединить
# count(sub[, start[, end]]) - Подсчет вхождений, где sub (обязательный) - подстрока для поиска (строка, количество вхождений которой нужно подсчитать), start (необязательный) - начальный индекс поиска, end (необязательный) - конечный индекс поиска